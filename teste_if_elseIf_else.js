let numero = prompt("Informe um número: ")
//let numero = 10
if (numero > 15){
    document.write("O número é maior que 5 mas menor ou igual a 15")}
else if(numero > 5){
    document.write("O número é maior que 5, mas menor ou igual a 15")
}
else if(numero >= 0){
    document.write("O número é maior ou igual a 0, mas menor ou igual a 5")
}
else {
    document.write("O número é negativo")
}